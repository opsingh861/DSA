class BubbleSortThread extends Thread {

  public static void bubbleSort(int[] arr) {
    int n = arr.length;

    for (int i = 0; i < n - 1; i++) { // n-1 because we are comparing i with i+1
      for (int j = 0; j < n - i - 1; j++) { // n-i-1 because we are comparing j with j+1
        if (arr[j] > arr[j + 1]) {
          // swap
          int temp = arr[j + 1];
          arr[j + 1] = arr[j];
          arr[j] = temp;
        }
      }
    }
  }

  public static void bubbleSortReverse(int[] arr) {
    int n = arr.length;

    for (int i = 0; i < n - 1; i++) { // n-1 because we are comparing i with i+1
      for (int j = 0; j < n - i - 1; j++) { // n-i-1 because we are comparing j with j+1
        if (arr[j] < arr[j + 1]) {
          // swap
          int temp = arr[j + 1];
          arr[j + 1] = arr[j];
          arr[j] = temp;
        }
      }
    }
  }
}

